# Base structure from en.json — only keys used by ReferralPage.tsx
def base_referral_page(
    title_variants: list[str],
    subtitle: str,
    how_title: str,
    steps: list[str],
    friend_title: str,
    friend_desc: str,
    reward_title: str,
    reward_desc: str,
    rows: list[tuple[str, str]],
    accrual_title: str,
    accrual_desc: str,
    timing_title: str,
    timing_desc: str,
    limits_title: str,
    limits_items: list[str],
    important_title: str,
    important_old: str,
    important_use_new: str,
    important_sources: list[str],
    reuse_title: str,
    reuse_desc: str,
    campaign_title: str,
    campaign_desc: str,
    share_title: str,
    share_text: str,
    micro: dict[str, str],
) -> dict:
    return {
        "hero": {
            "titleVariants": title_variants,
            "subtitle": subtitle,
            "mockPromo": "RAQOON-FRIEND",
        },
        "howItWorks": {"title": how_title, "steps": steps},
        "friendBonus": {"title": friend_title, "description": friend_desc},
        "yourReward": {
            "title": reward_title,
            "description": reward_desc,
            "rows": [{"period": p, "bonus": b} for p, b in rows],
        },
        "accrual": {"title": accrual_title, "description": accrual_desc},
        "timing": {"title": timing_title, "description": timing_desc},
        "limits": {"title": limits_title, "items": limits_items},
        "important": {
            "title": important_title,
            "inline": "{{title}}: {{oldCode}}",
            "oldCode": important_old,
            "useNew": important_use_new,
            "sources": important_sources,
        },
        "reuse": {"title": reuse_title, "description": reuse_desc},
        "campaign": {
            "title": campaign_title,
            "inline": "{{title}}. {{description}}",
            "description": campaign_desc,
        },
        "share": {"title": share_title, "text": share_text},
        "micro": micro,
    }



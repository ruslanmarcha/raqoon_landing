def merge_locale(filename: str, lang: str) -> None:
    path = LOCALES / filename
    data = json.loads(path.read_text(encoding="utf-8"))
    block = DATA[lang]
    data["meta"].update(block["meta"])
    # insert referralPage after downloadPage
    if "referralPage" in data:
        del data["referralPage"]
    keys = list(data.keys())
    di = keys.index("downloadPage")
    new_order = keys[: di + 1] + ["referralPage"] + keys[di + 1 :]
    data["referralPage"] = block["referralPage"]
    data = {k: data[k] for k in new_order}
    data["footer"]["referral"] = block["footerReferral"]
    path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


